    public VentanaCliente(String user) throws IOException {

        initComponents();
        this.setTitle("Ventana cliente - " + user);

        user_name = user;

        cliente = new Cliente(this);
        cliente.conexion(user);

        Vector<String> prueba = new Vector();
        prueba = cliente.pedirUsuarios();

        int contador = 0;

        for (int i = 0; i < prueba.size(); i++) {
            if (prueba.get(i).equals(user)) {
                contador++;
            }
        }

        if (contador > 1) {
            JOptionPane.showMessageDialog(null, "El usuario ya esta conectado");
            System.exit(0);
        } else {
            JOptionPane.showMessageDialog(null, "Bienvenid@ " + user);
            nomUsers = new Vector();
            ponerActivos(cliente.pedirUsuarios());
            ventPrivada = new VentanaPrivada(cliente);

            setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            setVisible(true);
        }
    }


