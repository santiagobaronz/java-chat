    public void actualizarChats(Vector datos) {
        nomUsers = datos;
        lstActivos.remove(0);
        ponerDatosList(this.lstActivos, nomUsers);
    }

    public void setNombreUser(String user) {
        nombreGrandeUser.setText("Chat general de " + user);
    }

    public void mostrarMsg(String msg) {
        this.panMostrar.append(msg + "\n\n\n");
    }

    public void ponerActivos(Vector datos) {
        cambiarNumero(datos);
        nomUsers = datos;
        ponerDatosList(this.lstActivos, nomUsers);
    }

    public void cambiarNumero(Vector datos) {
        this.users_conected.setText("Hay " + datos.size() + " usuarios conectados");
    }

    public void agregarUser(String user) {
        nomUsers.add(user);
        cambiarNumero(nomUsers);
        ponerDatosList(this.lstActivos, nomUsers);
    }

    public void retirraUser(String user) {
        nomUsers.remove(user);
        ponerDatosList(this.lstActivos, nomUsers);
    }

    private void ponerDatosList(JList list, final Vector datos) {
        list.setModel(new AbstractListModel() {
            @Override
            public int getSize() {
                return datos.size();
            }

            @Override
            public Object getElementAt(int i) {
                return datos.get(i);
            }
        });
    }

